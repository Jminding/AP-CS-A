public class Teacher {
    public String name;
    public int id;

    public Teacher() {
        this.name = "Mr. Leath";
        this.id = 69420;
    }

    public Teacher(String name) {
        this.name = name;
    }

    public Teacher(String name, int id) {
        this.name = name;
        this.id = id;
    }
}